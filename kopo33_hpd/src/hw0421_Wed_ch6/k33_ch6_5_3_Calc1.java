package hw0421_Wed_ch6;

public class k33_ch6_5_3_Calc1 {
	/*
	 * 메소드 오버로딩
	 */
	public int sum (int a, int b) {
		return a+b;
	}
	
	public int sum (int a, int b, int c) {
		return a+b+c;
	}
	
	public int sum (int a, int b, int c, int d) {
		return a+b+c+d;
	}
	
	public double sum (double a, double b) {
		return a+b;
	}
}
