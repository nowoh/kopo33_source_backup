package hw0410_Sat_ch2;

public class k33_ch2_실습_09 {
	/*
	 * CastingEx1
	 */
	public static void main(String[] args) {
		double k33_d = 85.4;			//실수형 변수 d를 85.4로 정의
		int k33_score = (int)k33_d;		//정수형 변수 score를 d를 integer로 형 변환해준 값으로 정의
		
		System.out.println("k33_score="+k33_score);		//score 출력
		System.out.println("k33_d="+k33_d);				//d 출력
	}
}
